package com.myschool.game.core;

/**
 * Cette classe est en fait une interface.
 * Une interface spécifie des méthodes sans les implémenter.
 * Les classes qui utilisent cette interface doivent implémenter toutes les méthodes de l'interface.
 */
public interface Fight {
    void getWeapon();

    void fight();

    void rangerWeapon();
}
